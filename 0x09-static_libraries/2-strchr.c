#include "main.h"
#include <stddef.h>

/**
 * _strchr - is a predefined function used for finding the occurrence of a character in a string.
 * @s: input
 * @c: input
 * Return: Always 0 (Success)
 */
char *_strchr(char *s, char c)
{
	int i = 0;

	for (; s[i] >= '\0'; i++)
	{
		if (s[i] == c)
			return (&s[i]);
	}
	return (0);
}
